def main():
    base = os.path.dirname(os.path.abspath(__file__))
    jobs_dir = os.path.join(base, "jobs")
    os.makedirs(jobs_dir, exist_ok=True)
    os.makedirs(os.path.join(base, "css"), exist_ok=True)

    # Generate index
    index_path = os.path.join(base, "index.html")
    with open(index_path, "w", encoding="utf-8") as f:
        f.write(generate_index())
    print(f"[OK] index.html generated")

    # Generate job pages
    for job in JOBS:
        slug = job[1]
        page_path = os.path.join(jobs_dir, f"{slug}.html")
        with open(page_path, "w", encoding="utf-8") as f:
            f.write(generate_job_page(job))
        print(f"[OK] jobs/{slug}.html generated")

    print(f"\nTotal: 1 index + {len(JOBS)} job pages = {1 + len(JOBS)} pages generated.")


if __name__ == "__main__":
    main()
